package com.example.manager.entity;

public class VeTau {
    private String maVe;
    private String maKH;
    private String maLichTrinh;
    private String loaiDoiTuong;
    private String maGhe;
    private double giaVe;
    private String trangThai;
    private String thoiDiemBanVe;
    private String maNhanVienBanVe;
    private String maHoaDon;

    public VeTau() {}

    public String getMaVe() { return maVe; }
    public void setMaVe(String m) { this.maVe = m; }
    public String getMaKH() { return maKH; }
    public void setMaKH(String m) { this.maKH = m; }
    public String getMaLichTrinh() { return maLichTrinh; }
    public void setMaLichTrinh(String m) { this.maLichTrinh = m; }
    public String getLoaiDoiTuong() { return loaiDoiTuong; }
    public void setLoaiDoiTuong(String l) { this.loaiDoiTuong = l; }
    public String getMaGhe() { return maGhe; }
    public void setMaGhe(String m) { this.maGhe = m; }
    public double getGiaVe() { return giaVe; }
    public void setGiaVe(double g) { this.giaVe = g; }
    public String getTrangThai() { return trangThai; }
    public void setTrangThai(String t) { this.trangThai = t; }
    public String getThoiDiemBanVe() { return thoiDiemBanVe; }
    public void setThoiDiemBanVe(String t) { this.thoiDiemBanVe = t; }
    public String getMaNhanVienBanVe() { return maNhanVienBanVe; }
    public void setMaNhanVienBanVe(String m) { this.maNhanVienBanVe = m; }
    public String getMaHoaDon() { return maHoaDon; }
    public void setMaHoaDon(String m) { this.maHoaDon = m; }
}